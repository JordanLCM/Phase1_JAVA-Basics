package Phase1_JB_3;

public class Phase1_JBasics005_DoWhileLoop {

	public static void main(String[] args) {

		int a = 1;
		do {
			System.out.println(a + ". Conditional Statement in Do While Loop");
			a++; //a = 1 + 1
		}while(a < 5);
		
		System.out.println("Do While Loop finished & stopped");
	}
}
